#include "libft.h"

int					ft_isalnum(int c)
{
	unsigned char	buf;

	buf = (unsigned char)c;
	if ((buf >= 65 && buf <= 90) || (buf >= 97 && buf <= 122) \
			|| (buf >= '0' && buf <= '9'))
		return (1);
	return (0);
}
