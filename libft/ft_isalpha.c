#include "libft.h"

int					ft_isalpha(int c)
{
	unsigned char	buf;

	buf = (unsigned char)c;
	if ((buf >= 65 && buf <= 90) || (buf >= 97 && buf <= 122))
		return (1);
	return (0);
}
