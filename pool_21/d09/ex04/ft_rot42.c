char	*ft_rot42(char *str)
{
	char *buffer;

	buffer = str;
	while (*str)
	{
		if ((*str >= 65 && *str <= 74) || (*str >= 97 && *str <= 106))
			*str += 16;
		else
		{
			if ((*str >= 75 && *str <= 90) || (*str >= 107 && *str <= 122))
				*str -= 10;
		}
		str++;
	}
	return (buffer);
}
