void	ft_print_comb(void)
{
	static char dig_[3];

	dig_[0] = '0';
	dig_[1] = '1';
	dig_[2] = '2';
	while (dig_[0] < '7')
	{
		while (dig_[1] < '9')
		{
			while(dig_[2] <= '9')
			{
				ft_putchar(dig_[0]);
				ft_putchar(dig_[1]);
				ft_putchar(dig_[2]);
				write (1, ", ", 2);
				dig_[2]++;
			}
			dig_[1]++;
			dig_[2] = dig_[1];
			dig_[2]++;
		}
		dig_[0]++;
		dig_[1] = dig_[0];
	}
	write(1, "789", 3);
}
