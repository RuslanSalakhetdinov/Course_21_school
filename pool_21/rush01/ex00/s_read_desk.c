#include "sudoku.h"

void	s_read_desk(char **argv, char desk[9][9])
{
	int x;
	int y;

	x = 0;
	while (x < 9)
	{
		y = 0;
		while (y < 9)
		{
			if (argv[x + 1][y] == '.')
				desk[x][y] = '0';
			else
				desk[x][y] = argv[x + 1][y];
			y++;
		}
		x++;
	}
}
