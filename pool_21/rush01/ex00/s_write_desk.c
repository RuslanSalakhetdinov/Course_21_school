#include "sudoku.h"

void	s_write_desk(char desk[9][9])
{
	int		i;
	int		j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 8)
		{
			write(1, &desk[i][j], 1);
			write(1, " ", 1);
			j++;
		}
		write(1, &desk[i][8], 1);
		write(1, "\n", 1);
		i++;
	}
}
