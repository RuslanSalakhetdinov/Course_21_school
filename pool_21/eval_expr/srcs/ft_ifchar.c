#include "eval.h"

int		ft_ifchar(char c)
{
	if (c == ' ')
		return (-1);
	if (c == '+')
		return (0);
	if (c == '-')
		return (1);
	if (c == '/')
		return (2);
	if (c == '%')
		return (3);
	if (c == '*')
		return (4);
	if (c == '(')
		return (5);
	if (c == ')')
		return (6);
	if (c >= '0' && c <= '9')
		return (7);
	return (-42);
}
